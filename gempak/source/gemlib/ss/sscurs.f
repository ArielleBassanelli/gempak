	SUBROUTINE SS_CURS  ( line, iret )
C************************************************************************
C* SS_CURS								*
C*									*
C* This subroutine moves the cursor to the beginning of the specified	*
C* line.								*
C*									*
C* SS_CURS  ( LINE, IRET )						*
C*									*
C* Input parameters:							*
C*	LINE		INTEGER		Line on which to put cursor	*
C*									*
C* Output parameters:							*
C*	IRET		INTEGER		Return code			*
C*					  0 = normal return		*
C*					 -1 = system service error	*
C**									*
C* Log: 								*
C* I. Graffman/RDS	10/87						*
C************************************************************************
	iret = 0
C*
	RETURN
	END
